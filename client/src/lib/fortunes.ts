export function getFortune(): { text: string; isRare: boolean; isGlitch: boolean } {
  const r = Math.random();
  if (r < 0.02) {
    const text = GLITCH_FORTUNES[Math.floor(Math.random() * GLITCH_FORTUNES.length)];
    return { text, isRare: false, isGlitch: true };
  }
  if (r < 0.06) {
    const text = RARE_FORTUNES[Math.floor(Math.random() * RARE_FORTUNES.length)];
    return { text, isRare: true, isGlitch: false };
  }
  const text = FORTUNES[Math.floor(Math.random() * FORTUNES.length)];
  return { text, isRare: false, isGlitch: false };
}

export function getMemoryFortune(visits: number, daysSinceLast: number): string | null {
  if (daysSinceLast > 7) {
    const pool = MEMORY_MESSAGES.longAbsence;
    return pool[Math.floor(Math.random() * pool.length)];
  }
  if (visits > 10) {
    const pool = MEMORY_MESSAGES.frequent;
    return pool[Math.floor(Math.random() * pool.length)];
  }
  if (visits > 1) {
    const pool = MEMORY_MESSAGES.returning;
    return pool[Math.floor(Math.random() * pool.length)];
  }
  return null;
}
